def platform_paths(
    ctx: SyncContext,
    platform: Platform,
    scope: Literal["user", "repo"],
    repo_root: Path | None,
) -> dict[str, object]:
    if scope == "repo" and repo_root is None:
        raise ValueError("repo scope requires a repo root")

    if platform == "claude":
        base = ctx.home / ".claude" if scope == "user" else repo_root / ".claude"
        instruction_file = ctx.home / ".claude" / "CLAUDE.md" if scope == "user" else repo_root / "CLAUDE.md"
        return {
            "platform": platform,
            "scope": scope,
            "base_dir": base,
            "instruction_file": instruction_file,
            "instruction_candidates": [
                instruction_file,
                (repo_root / ".claude" / "CLAUDE.md") if scope == "repo" else base / "CLAUDE.md",
            ],
            "settings_file": base / "settings.json",
            "settings_local_file": base / "settings.local.json",
            "rules_dir": base / "rules",
            "skills_dir": base / "skills",
            "agents_dir": base / "agents",
            "unsupported_dir": base / MANAGED_PREFIX / "unsupported",
        }

    base = ctx.home / ".codex" if scope == "user" else repo_root / ".codex"
    home_codex_skills_dir = ctx.home / ".codex" / "skills"
    home_agents_dir = ctx.home / ".agents" / "skills"
    repo_agents_dir = repo_root / ".agents" / "skills" if repo_root else None
    repo_codex_skills_dir = repo_root / ".codex" / "skills" if repo_root else None
    skills_dirs: list[Path] = []
    managed_skills_dirs: list[Path] = []
    if scope == "user":
        skills_dirs.append(home_agents_dir)
        managed_skills_dirs.append(home_agents_dir)
        if home_codex_skills_dir.exists():
            skills_dirs.append(home_codex_skills_dir)
    else:
        if repo_agents_dir is not None:
            skills_dirs.extend([repo_agents_dir])
            managed_skills_dirs.extend([repo_agents_dir])
        if repo_codex_skills_dir is not None and repo_codex_skills_dir.exists():
            skills_dirs.append(repo_codex_skills_dir)

    source_skills_dirs: list[Path] = [p for p in skills_dirs if p is not None]
    return {
        "platform": platform,
        "scope": scope,
        "base_dir": base,
        "instruction_file": ctx.home / ".codex" / "AGENTS.md" if scope == "user" else repo_root / "AGENTS.md",
        "config_file": base / "config.toml",
        "rules_dir": base / "rules",
        "skills_dirs": source_skills_dirs,
        "managed_skills_dirs": managed_skills_dirs,
        "unsupported_dir": base / MANAGED_PREFIX / "unsupported",
    }


def sync_skill_trees(
    source_skill_dirs: list[Path],
    target_skill_dirs: list[Path],
    source_platform: Platform,
    target_platform: Platform,
) -> list[Operation]:
    operations: list[Operation] = []
    seen: set[str] = set()
    for source_root in source_skill_dirs:
        if not source_root.exists():
            continue
        for skill_dir in sorted(source_root.iterdir()):
            if not skill_dir.is_dir():
                continue
            if not is_syncable_source_skill(source_root, skill_dir, source_platform):
                continue
            if skill_dir.name == SELF_SKILL_NAME:
                continue
            if skill_dir.name in seen:
                continue
            skill_md = skill_dir / "SKILL.md"
            if not skill_md.exists():
                continue
            seen.add(skill_dir.name)
            for target_root in target_skill_dirs:
                if target_root is None:
                    continue
                target_dir = target_root / skill_dir.name
                staged_dir = stage_skill_tree(skill_dir, target_dir, source_platform, target_platform)
                operations.append(
                    Operation(
                        kind="copy_tree",
                        path=target_dir,
                        description=f"Sync skill {skill_dir.name} to {target_dir}",
                        source=staged_dir,
                        source_platform=source_platform,
                        target_platform=target_platform,
                    )
                )
    return operations


def is_syncable_source_skill(source_root: Path, skill_dir: Path, source_platform: Platform) -> bool:
    if skill_dir.name.startswith("."):
        return False
    return True


def stage_skill_tree(
    source_dir: Path,
    target_dir: Path,
    source_platform: Platform,
    target_platform: Platform,
) -> Path:
    temp_root = Path(
        tempfile.mkdtemp(
            prefix=f"{MANAGED_PREFIX}-{target_platform}-",
            dir=Path(os.environ.get("TMPDIR", "/tmp")).resolve(),
        )
    )
    staged_dir = temp_root / source_dir.name
    staged_dir.mkdir(parents=True)

    frontmatter, body = parse_frontmatter(read_text(source_dir / "SKILL.md"))
    generated_frontmatter = {
        "name": frontmatter.get("name") or source_dir.name,
        "description": frontmatter.get("description") or first_paragraph(body) or f"Synchronized skill from {source_platform}",
    }

    if target_platform == "claude":
        if source_platform == "codex" and codex_skill_disables_implicit_invocation(source_dir):
            generated_frontmatter["disable-model-invocation"] = "true"
        skill_text = format_frontmatter(generated_frontmatter) + body
    else:
        skill_text = format_frontmatter(generated_frontmatter) + body

    write_text(staged_dir / "SKILL.md", skill_text)
    copy_skill_payload(source_dir, staged_dir, target_platform)

    if target_platform == "codex":
        write_codex_openai_yaml(
            staged_dir / "agents" / "openai.yaml",
            source_dir,
            generated_frontmatter["name"],
            generated_frontmatter["description"],
            source_platform == "claude" and is_truthy(frontmatter.get("disable-model-invocation", "")),
        )
    return staged_dir


def copy_skill_payload(source_dir: Path, staged_dir: Path, target_platform: Platform) -> None:
    for item in source_dir.iterdir():
        if item.name in SKILL_COPY_EXCLUDES:
            continue
        destination = staged_dir / item.name
        if item.is_dir():
            shutil.copytree(item, destination, dirs_exist_ok=True)
        else:
            shutil.copy2(item, destination)


def write_codex_openai_yaml(
    path: Path,
    source_dir: Path,
    name: str,
    description: str,
    disable_implicit: bool,
) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    content = (
        "interface:\n"
        f'  display_name: "{name}"\n'
        f'  short_description: "{escape_yaml(description)}"\n'
        "policy:\n"
        f"  allow_implicit_invocation: {'false' if disable_implicit else 'true'}\n"
    )
    write_text(path, content)


def codex_skill_disables_implicit_invocation(skill_dir: Path) -> bool:
    yaml_path = skill_dir / "agents" / "openai.yaml"
    if not yaml_path.exists():
        return False
    for line in read_text(yaml_path).splitlines():
        if "allow_implicit_invocation" in line:
            return line.split(":", 1)[1].strip().lower() == "false"
    return False


def render_plan(
    plan: list[Operation],
    source: Platform,
    target: Platform,
    scope: Scope,
    repo_root: Path | None,
    apply: bool,
    backup_root: Path | None = None,
) -> str:
    lines = [
        f"direction: {source} -> {target}",
        f"scope: {scope}",
        f"repo_root: {repo_root if repo_root else '-'}",
        f"mode: {'apply' if apply else 'diff'}",
    ]
    if backup_root:
        lines.append(f"backup_root: {backup_root}")
    lines.append(f"changes: {len(plan)}")
    for op in plan:
        lines.append(f"- [{op.kind}] {op.path}: {op.description}")
    return "\n".join(lines)


def apply_plan(plan: list[Operation], backup_root: Path, force: bool) -> None:
    validate_plan(plan, force=force)
    backup_root.mkdir(parents=True, exist_ok=True)
    for op in plan:
        if op.kind == "write_text":
            if op.path.exists():
                backup_existing(op.path, backup_root)
            op.path.parent.mkdir(parents=True, exist_ok=True)
            write_text(op.path, op.content or "")
        elif op.kind == "copy_tree":
            if op.path.exists():
                backup_existing(op.path, backup_root)
                shutil.rmtree(op.path)
            op.path.parent.mkdir(parents=True, exist_ok=True)
            shutil.copytree(op.source, op.path)
            marker = {
                "generated_by": MANAGED_PREFIX,
                "source_platform": op.source_platform,
                "target_platform": op.target_platform,
                "source": str(op.source) if op.source else None,
                "generated_at": timestamp(),
            }
            write_text(op.path / MARKER_FILENAME, json.dumps(marker, indent=2))
        elif op.kind == "remove_path":
            if op.path.exists():
                backup_existing(op.path, backup_root)
                if op.path.is_dir():
                    shutil.rmtree(op.path)
                else:
                    op.path.unlink()
        else:  # pragma: no cover
            raise ValueError(f"unsupported operation kind: {op.kind}")


def validate_plan(plan: list[Operation], force: bool) -> None:
    conflicts = [
        op.path
        for op in plan
        if op.kind == "copy_tree" and op.path.exists() and not force and not is_managed_tree(op.path)
    ]
    if not conflicts:
        return

    formatted = "\n".join(f"- {path}" for path in conflicts)
    raise SystemExit(
        "Refusing to overwrite unmanaged skill targets without --force:\n"
        f"{formatted}"
    )


def is_managed_tree(path: Path) -> bool:
    if not path.is_dir():
        return False
    marker_path = path / MARKER_FILENAME
    if not marker_path.exists():
        return False
    try:
        marker = json.loads(read_text(marker_path))
    except (json.JSONDecodeError, OSError):
        return False
    return marker.get("generated_by") == MANAGED_PREFIX


def backup_existing(path: Path, backup_root: Path) -> None:
    target = backup_root / sanitize_path(path)
    target.parent.mkdir(parents=True, exist_ok=True)
    if path.is_dir():
        shutil.copytree(path, target, dirs_exist_ok=True)
    else:
        shutil.copy2(path, target)


def sanitize_path(path: Path) -> Path:
    pieces = [part for part in path.parts if part not in (path.anchor, "/")]
    return Path(*pieces)


def default_backup_root(ctx: SyncContext, scope: Scope, repo_root: Path | None) -> Path:
    stamp = timestamp(compact=True)
    if scope == "repo" and repo_root:
        return repo_root / f".{MANAGED_PREFIX}" / "backups" / stamp
    if scope == "all" and repo_root:
        return repo_root / f".{MANAGED_PREFIX}" / "backups" / stamp
    return ctx.home / f".{MANAGED_PREFIX}" / "backups" / stamp


def note_operation(target: Path, source: Path, summary: str, target_platform: Platform) -> Operation:
    if source.is_dir():
        body = [summary, "", f"Source directory: {source}", ""]
        for path in sorted(source.rglob("*")):
            if path.is_file():
                body.append(f"## {path.relative_to(source)}")
                body.append("")
                if path.suffix.lower() in TEXT_EXTENSIONS:
                    body.append(read_text(path))
                else:
                    body.append(f"[binary file: {path.name}]")
                body.append("")
        content = "\n".join(body).rstrip() + "\n"
    else:
        content = summary + "\n\n"
        content += f"Source file: {source}\n\n"
        content += read_optional_text(source)
    return Operation(
        kind="write_text",
        path=target,
        description=f"Write unsupported-config note {target.name}",
        content=content,
        source=source,
        target_platform=target_platform,
    )


def render_rules_bundle(rules_dir: Path) -> str:
    if not rules_dir.exists():
        return ""
    sections: list[str] = []
    for path in sorted(rules_dir.glob("*.md")):
        sections.append(f"## {path.name}\n\n{read_text(path).strip()}\n")
    return "\n".join(sections).strip()


def render_import_block(title: str, body: str) -> str:
    return f"{title}\n\n{body.strip()}\n"


def parse_frontmatter(text: str) -> tuple[dict[str, str], str]:
    if not text.startswith("---\n"):
        return {}, text
    lines = text.splitlines(keepends=True)
    if len(lines) < 3:
        return {}, text

    end_index = None
    for idx in range(1, len(lines)):
        if lines[idx].strip() == "---":
            end_index = idx
            break
    if end_index is None:
        return {}, text

    frontmatter: dict[str, str] = {}
    for raw in lines[1:end_index]:
        line = raw.strip()
        if not line or line.startswith("#") or ":" not in line:
            continue
        key, value = line.split(":", 1)
        frontmatter[key.strip()] = strip_yaml_string(value.strip())
    body = "".join(lines[end_index + 1 :])
    return frontmatter, body


def format_frontmatter(frontmatter: dict[str, str]) -> str:
    lines = ["---"]
    for key, value in frontmatter.items():
        lines.append(f"{key}: {yaml_scalar(value)}")
    lines.append("---")
    return "\n".join(lines) + "\n\n"


def first_paragraph(text: str) -> str:
    for block in text.split("\n\n"):
        stripped = block.strip()
        if stripped:
            return stripped.splitlines()[0]
    return ""


def strip_yaml_string(value: str) -> str:
    if len(value) >= 2 and value[0] == value[-1] and value[0] in {'"', "'"}:
        return value[1:-1]
    return value


def yaml_scalar(value: str) -> str:
    if re.fullmatch(r"[A-Za-z0-9._/-]+", value):
        return value
    escaped = value.replace("\\", "\\\\").replace('"', '\\"')
    return f'"{escaped}"'


def escape_yaml(value: str) -> str:
    return value.replace("\\", "\\\\").replace('"', '\\"')


def is_truthy(value: str) -> bool:
    return value.lower() in {"1", "true", "yes", "on"}


def ensure_codex_fallbacks(existing: str) -> str:
    lines = existing.splitlines() if existing else []
    found_project_docs = False
    updated: list[str] = []
    for line in lines:
        stripped = line.strip()
        if stripped.startswith("project_doc_fallback_filenames"):
            found_project_docs = True
            values = parse_toml_array(stripped)
            if "CLAUDE.md" not in values:
                values.append("CLAUDE.md")
            updated.append(f'project_doc_fallback_filenames = [{", ".join(json.dumps(v) for v in values)}]')
        else:
            updated.append(line)
    if not found_project_docs:
        if updated and updated[-1].strip():
            updated.append("")
        updated.append('project_doc_fallback_filenames = ["CLAUDE.md"]')
    if not any(line.strip().startswith("project_doc_max_bytes") for line in updated):
        updated.append("project_doc_max_bytes = 65536")
    return "\n".join(updated).rstrip() + "\n"


def parse_toml_array(line: str) -> list[str]:
    _, raw = line.split("=", 1)
    raw = raw.strip()
    if raw.startswith("[") and raw.endswith("]"):
        body = raw[1:-1].strip()
        if not body:
            return []
        return [strip_yaml_string(part.strip()) for part in body.split(",")]
    return []


def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def read_optional_text(path: Path) -> str:
    if not path.exists():
        return ""
    return read_text(path)


def write_text(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def first_existing_path(paths: Iterable[Path]) -> Path | None:
    for path in paths:
        if path.exists():
            return path
    return None


def collect_snapshot(ctx: SyncContext, repo_root: Path | None) -> dict[str, object]:
    data: dict[str, object] = {}
    for platform in ("claude", "codex"):
        platform_data: dict[str, object] = {}
        for scope in ("user", "repo"):
            if scope == "repo" and repo_root is None:
                continue
            paths = platform_paths(ctx, platform, scope, repo_root)
            scope_data: dict[str, object] = {}
            for key, value in paths.items():
                if isinstance(value, Path):
                    scope_data[key] = {"path": str(value), "exists": value.exists()}
                elif isinstance(value, list):
                    scope_data[key] = [{"path": str(p), "exists": p.exists()} for p in value]
            platform_data[scope] = scope_data
        data[platform] = platform_data
    return data


def pending_text_for_path(
    operations: list[Operation],
    path: Path,
    fallback_path: Path,
) -> str:
    for op in reversed(operations):
        if op.path == path and op.kind == "write_text":
            return op.content or ""
    return read_optional_text(fallback_path)


def dedupe_last_write(operations: list[Operation]) -> list[Operation]:
    deduped: list[Operation] = []
    latest_write_index: dict[Path, int] = {}
    for op in operations:
        if op.kind == "write_text":
            latest_write_index[op.path] = len(deduped)
            deduped.append(op)
        else:
            deduped.append(op)
    final_ops: list[Operation] = []
    for idx, op in enumerate(deduped):
        if op.kind == "write_text" and latest_write_index.get(op.path) != idx:
            continue
        final_ops.append(op)
    return final_ops


def timestamp(compact: bool = False) -> str:
    fmt = "%Y%m%d-%H%M%S" if compact else "%Y-%m-%dT%H:%M:%SZ"
    return datetime.now(timezone.utc).strftime(fmt)


def upsert_managed_block(existing: str, name: str, content: str) -> str:
    begin = f"<!-- {MANAGED_PREFIX}:begin {name} -->"
    end = f"<!-- {MANAGED_PREFIX}:end {name} -->"
    block = f"{begin}\n{content.rstrip()}\n{end}"
    pattern = re.compile(
        rf"{re.escape(begin)}.*?{re.escape(end)}",
        re.DOTALL,
    )

    if pattern.search(existing):
        updated = pattern.sub(block, existing)
    elif existing.strip():
        updated = existing.rstrip() + "\n\n" + block + "\n"
    else:
        updated = block + "\n"
    return updated


