@dataclass
class SyncContext:
    cwd: Path
    home: Path
    current_platform: Platform | None = None


@dataclass
class Operation:
    kind: OperationKind
    path: Path
    description: str
    content: str | None = None
    source: Path | None = None
    source_platform: Platform | None = None
    target_platform: Platform | None = None
    metadata: dict[str, str] = field(default_factory=dict)


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    ctx = SyncContext(
        cwd=Path.cwd(),
        home=Path.home(),
        current_platform=os.environ.get(CURRENT_PLATFORM_ENV) or None,
    )

    if args.command == "status":
        return cmd_status(ctx, args)
    if args.command == "doctor":
        return cmd_doctor(ctx, args)
    if args.command == "sync":
        return cmd_sync(ctx, args, implicit=False)
    if args.command in {"import", "export"}:
        return cmd_sync(ctx, args, implicit=True)
    parser.error(f"unknown command: {args.command}")
    return 2


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="claude-codex-sync",
        description="Synchronize Claude Code and Codex instructions, skills, and supported config.",
    )
    subparsers = parser.add_subparsers(dest="command", required=True)

    status = subparsers.add_parser("status", help="Show detected configuration for both platforms")
    add_scope_args(status)
    status.add_argument("--repo-root", type=Path, help="Explicit repository root")

    doctor = subparsers.add_parser("doctor", help="Explain what can and cannot be synchronized")
    add_scope_args(doctor)
    doctor.add_argument("--repo-root", type=Path, help="Explicit repository root")

    sync = subparsers.add_parser("sync", help="Diff or apply synchronization between platforms")
    sync.add_argument("--from", dest="source_platform", choices=["claude", "codex"], required=True)
    sync.add_argument("--to", dest="target_platform", choices=["claude", "codex"], required=True)
    sync.add_argument("--apply", action="store_true", help="Apply changes instead of only showing a diff")
    sync.add_argument(
        "--force",
        action="store_true",
        help="Allow replacing existing skill targets that were not generated by this tool",
    )
    add_scope_args(sync)
    sync.add_argument("--repo-root", type=Path, help="Explicit repository root")

    imp = subparsers.add_parser(
        "import",
        help="Import another platform's configuration into the current platform",
    )
    imp.add_argument("source_platform", choices=["claude", "codex"])
    imp.add_argument("--apply", action="store_true", help="Apply changes instead of only showing a diff")
    imp.add_argument(
        "--force",
        action="store_true",
        help="Allow replacing existing skill targets that were not generated by this tool",
    )
    add_scope_args(imp)
    imp.add_argument("--repo-root", type=Path, help="Explicit repository root")

    exp = subparsers.add_parser(
        "export",
        help="Export the current platform's configuration into the other platform",
    )
    exp.add_argument("target_platform", choices=["claude", "codex"])
    exp.add_argument("--apply", action="store_true", help="Apply changes instead of only showing a diff")
    exp.add_argument(
        "--force",
        action="store_true",
        help="Allow replacing existing skill targets that were not generated by this tool",
    )
    add_scope_args(exp)
    exp.add_argument("--repo-root", type=Path, help="Explicit repository root")

    return parser


def add_scope_args(parser: argparse.ArgumentParser) -> None:
    parser.add_argument(
        "--scope",
        choices=["auto", "user", "repo", "all"],
        default="auto",
        help="Select user-level, repo-level, or both configuration scopes",
    )


def cmd_status(ctx: SyncContext, args: argparse.Namespace) -> int:
    repo_root = resolve_repo_root(ctx.cwd, args.repo_root)
    scope = effective_scope(ctx.cwd, args.scope, repo_root)
    snapshot = collect_snapshot(ctx, repo_root)
    print(json.dumps({"scope": scope, "repo_root": str(repo_root) if repo_root else None, **snapshot}, indent=2))
    return 0


def cmd_doctor(ctx: SyncContext, args: argparse.Namespace) -> int:
    repo_root = resolve_repo_root(ctx.cwd, args.repo_root)
    scope = effective_scope(ctx.cwd, args.scope, repo_root)
    snapshot = collect_snapshot(ctx, repo_root)

    report = {
        "scope": scope,
        "repo_root": str(repo_root) if repo_root else None,
        "supported": [
            "CLAUDE.md <-> AGENTS.md",
            ".claude/skills <-> Codex skills directories",
            "Codex config fallback list for CLAUDE.md",
            "Best-effort notes for unsupported platform-only config",
        ],
        "unsupported": [
            "Claude hooks in settings.json have no native Codex equivalent",
            "Codex .rules and approval policy have no native Claude equivalent",
            "Claude subagents are copied as notes because they are not file-compatible",
            "Local runtime/session state is not synchronized",
        ],
        "detected": snapshot,
    }
    print(json.dumps(report, indent=2))
    return 0


def cmd_sync(ctx: SyncContext, args: argparse.Namespace, implicit: bool) -> int:
    repo_root = resolve_repo_root(ctx.cwd, args.repo_root)
    scope = effective_scope(ctx.cwd, args.scope, repo_root)
    source, target = resolve_direction(ctx.current_platform, args, implicit)
    if source == target:
        raise SystemExit("source and target platforms must be different")

    plan = build_sync_plan(ctx, source, target, scope, repo_root)
    if not args.apply:
        print(render_plan(plan, source, target, scope, repo_root, apply=False))
        return 0

    backup_root = default_backup_root(ctx, scope, repo_root)
    apply_plan(plan, backup_root=backup_root, force=args.force)
    print(render_plan(plan, source, target, scope, repo_root, apply=True, backup_root=backup_root))
    return 0


def resolve_direction(
    current_platform: Platform | None,
    args: argparse.Namespace,
    implicit: bool,
) -> tuple[Platform, Platform]:
    if not implicit:
        return args.source_platform, args.target_platform
    if current_platform not in {"claude", "codex"}:
        raise SystemExit(f"implicit import/export requires {CURRENT_PLATFORM_ENV} to be set by the wrapper")
    if args.command == "import":
        return args.source_platform, current_platform
    return current_platform, args.target_platform


def resolve_repo_root(cwd: Path, explicit: Path | None) -> Path | None:
    if explicit:
        return explicit.expanduser().resolve()

    cur = cwd.resolve()
    fallback: Path | None = None
    while True:
        if (cur / ".git").exists():
            return cur
        if any((cur / marker).exists() for marker in ("CLAUDE.md", "AGENTS.md", ".claude", ".agents", ".codex")):
            fallback = cur
        if cur.parent == cur:
            return fallback
        cur = cur.parent


def effective_scope(cwd: Path, requested: Scope, repo_root: Path | None) -> Scope:
    if requested != "auto":
        return requested
    return "all" if repo_root else "user"


def build_sync_plan(
    ctx: SyncContext,
    source: Platform,
    target: Platform,
    scope: Scope,
    repo_root: Path | None,
) -> list[Operation]:
    operations: list[Operation] = []
    scopes = expand_scopes(scope, repo_root)
    for current_scope in scopes:
        if current_scope == "user":
            operations.extend(build_scope_plan(ctx, source, target, "user", None))
        elif current_scope == "repo":
            if repo_root is None:
                continue
            operations.extend(build_scope_plan(ctx, source, target, "repo", repo_root))
    return operations


def expand_scopes(scope: Scope, repo_root: Path | None) -> list[Literal["user", "repo"]]:
    if scope == "user":
        return ["user"]
    if scope == "repo":
        return ["repo"] if repo_root else []
    return ["user", "repo"] if repo_root else ["user"]


def build_scope_plan(
    ctx: SyncContext,
    source: Platform,
    target: Platform,
    scope: Literal["user", "repo"],
    repo_root: Path | None,
) -> list[Operation]:
    operations: list[Operation] = []
    src = platform_paths(ctx, source, scope, repo_root)
    dst = platform_paths(ctx, target, scope, repo_root)

    if source == "claude" and target == "codex":
        operations.extend(sync_claude_to_codex(ctx, src, dst, scope))
    elif source == "codex" and target == "claude":
        operations.extend(sync_codex_to_claude(ctx, src, dst, scope))
    else:
        raise ValueError(f"unsupported direction: {source} -> {target}")
    return operations


def sync_claude_to_codex(
    ctx: SyncContext,
    src: dict[str, object],
    dst: dict[str, object],
    scope: Literal["user", "repo"],
) -> list[Operation]:
    operations: list[Operation] = []
    claude_instruction = first_existing_path(src["instruction_candidates"])
    rules_dir = src["rules_dir"]
    settings = src["settings_file"]
    settings_local = src.get("settings_local_file")
    agents_dir = src["agents_dir"]

    if claude_instruction:
        block_name = f"claude-{scope}-claude-md"
        content = render_import_block(
            title=f"Imported from {claude_instruction}",
            body=read_text(claude_instruction),
        )
        operations.append(
            Operation(
                kind="write_text",
                path=dst["instruction_file"],
                description=f"Update {dst['instruction_file']} with {claude_instruction.name}",
                content=upsert_managed_block(
                    read_optional_text(dst["instruction_file"]),
                    block_name,
                    content,
                ),
                source=claude_instruction,
                source_platform="claude",
                target_platform="codex",
            )
        )

    rule_content = render_rules_bundle(rules_dir)
    if rule_content:
        block_name = f"claude-{scope}-rules"
        operations.append(
            Operation(
                kind="write_text",
                path=dst["instruction_file"],
                description=f"Import Claude rules into {dst['instruction_file']}",
                content=upsert_managed_block(
                    pending_text_for_path(operations, dst["instruction_file"], dst["instruction_file"]),
                    block_name,
                    render_import_block(
                        title=f"Imported Claude rules from {rules_dir}",
                        body=rule_content,
                    ),
                ),
                source=rules_dir,
                source_platform="claude",
                target_platform="codex",
            )
        )

    operations.append(
        Operation(
            kind="write_text",
            path=dst["config_file"],
            description=f"Ensure {dst['config_file']} loads CLAUDE.md as a fallback project doc",
            content=ensure_codex_fallbacks(read_optional_text(dst["config_file"])),
            source=settings if settings.exists() else None,
            source_platform="claude",
            target_platform="codex",
        )
    )

    if settings.exists():
        operations.append(
            note_operation(
                dst["unsupported_dir"] / f"claude-{scope}-settings.json",
                settings,
                "Claude settings were copied as notes because Codex has no direct settings.json equivalent.",
                target_platform="codex",
            )
        )

    if settings_local and settings_local.exists():
        operations.append(
            note_operation(
                dst["unsupported_dir"] / f"claude-{scope}-settings.local.json",
                settings_local,
                "Claude local settings were copied as notes because Codex has no direct local settings equivalent.",
                target_platform="codex",
            )
        )

    if agents_dir.exists():
        operations.append(
            note_operation(
                dst["unsupported_dir"] / f"claude-{scope}-agents.md",
                agents_dir,
                "Claude subagents were copied as notes because Codex custom agents are not file-compatible.",
                target_platform="codex",
            )
        )

    operations.extend(
        sync_skill_trees(
            source_skill_dirs=[src["skills_dir"]],
            target_skill_dirs=dst["managed_skills_dirs"],
            source_platform="claude",
            target_platform="codex",
        )
    )
    return dedupe_last_write(operations)


